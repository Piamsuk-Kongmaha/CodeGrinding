#include <bits/stdc++.h>

using namespace std;

int main()
{
    int n = 4;
    int g = n+3;
    int l = n+2;
    //cin >> n;
    int temp = n+1;
    
    while (temp--)
    {
    for(int i =n+3; i > 1;i --)
    {
        for(int j = n+3; j > i; j--){
            cout << " ";
        }
        for(int j = 0; j < i; j++)
        {
            if(i == n+3 || j == 0 || j == i-1) cout << "* ";
            else if(l >= i) cout << ". ";
            else cout << "  ";
        }
        cout << '\n';
    }
    l--;
    for(int i =2; i <= n+3;i++)
    {
        for(int j = n+3; j > i; j--){
            cout << " ";
        }
        for(int j = 0; j < i; j++)
        {
            if(j == 0 || j== i-1 || i == n+3)cout << "* ";
            else if(g <= i) cout << ". ";
            else cout << "  ";
        }
        cout << '\n';
    }
    g--;
    cout << '\n';
    _sleep(2000);
    }

}
